def fallback_lang(lang: str | None) -> str:
    if not lang:
        return "en"
    if lang not in SUPPORTED_LANGS:
        return "en"
    return lang


def pick(lang: str, key: str) -> str:
    lang = fallback_lang(lang)
    variations = TEXT.get(lang, {}).get(key) or TEXT["en"].get(key) or [""]
    if not variations:
        return ""
    return random.choice(variations)


def get_first(lang: str, key: str) -> str:
    lang = fallback_lang(lang)
    variations = TEXT.get(lang, {}).get(key) or TEXT["en"].get(key) or [""]
    return variations[0] if variations else ""

